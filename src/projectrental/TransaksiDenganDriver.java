    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Kode = jTextPane5.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            koneksi = DriverManager.getConnection(DB_URL, USER, PASS);
            String query = "select * from data_mobil where kode_mobil = '"+ Kode +"' and keterangan = 'Tersedia'";
            stmt = koneksi.createStatement();

            ResultSet hasilQuery = stmt.executeQuery(query);
            while (hasilQuery.next()) {
            jTextPane6.setText(hasilQuery.getString("merk_mobil"));
            jTextPane7.setText(hasilQuery.getString("nopol"));
            jTextPane8.setText(hasilQuery.getString("harga"));
            }
            koneksi.close();
            stmt.close();
            hasilQuery.close();
        
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

